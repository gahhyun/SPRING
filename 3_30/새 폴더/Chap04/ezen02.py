# 홀수-짝수 판별 프로그램을 작성하시오.
'''
    정수를 입력하시오 : 10 
    짝수입니다.
    
    정수를 입력하시오 : 17 
    홀수입니다.    
    
'''
num = int(input("정수를 입력하시오 : "))
