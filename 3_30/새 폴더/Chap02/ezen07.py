'''
    * 번개가 치고 천둥소리가 들리기까지의 시간을 입력하여
      자신의 위치에서 천둥번개가 발생한 장소까지의 거리를
      계산하는 프로그램을 작성하시오.

      ( 빛의 속도 = 약 30만 km /s
        소리의 속도 = 약 340 m/s )

      측정 시간(초) 입력 : 3
      자신의 위치에서 번개가 친 장소까지의 거리 :    m
'''
sec = int(input("측정 시간(초) 입력 : "))
distance = 340 * sec
print("자신의 위치에서 번개가 친 장소까지의 거리 :", distance, "m")




















